<script lang="ts">
	import type { AppID } from '🍎/state/apps.svelte';

	const {
		app_id,
		is_being_dragged,
	}: {
		app_id: AppID;
		is_being_dragged: boolean;
	} = $props();
</script>

{#if app_id === 'calendar'}
	{#await import('./Calendar/Calendar.svelte') then { default: Calendar }}
		<Calendar />
	{/await}
{:else if app_id === 'vscode'}
	{#await import('./VSCode/VSCode.svelte') then { default: VSCode }}
		<VSCode {is_being_dragged} />
	{/await}
{:else if app_id === 'calculator'}
	{#await import('./Calculator/Calculator.svelte') then { default: Calculator }}
		<Calculator />
	{/await}
{:else if app_id === 'wallpapers'}
	{#await import('./WallpaperApp/WallpaperSelectorApp.svelte') then { default: WallpaperSelector }}
		<WallpaperSelector />
	{/await}
{:else if app_id === 'orpheus-twitter'}
	{#await import('./OrpheusProfile/OrpheusProfile.svelte') then { default: OrpheusProfile }}
		<OrpheusProfile />
	{/await}
{:else if app_id === 'pro-network'}
	{#await import('./ProNetwork/ProNetwork.svelte') then { default: ProNetwork }}
		<ProNetwork />
	{/await}
{:else if app_id === 'exposure-hub'}
	{#await import('./ExposureHub/ExposureHub.svelte') then { default: ExposureHub }}
		<ExposureHub />
	{/await}
{:else}
	{#await import('./AppStore/AppStore.svelte') then { default: AppStore }}
		<AppStore {app_id} />
	{/await}
{/if}
